public class Lesson5 {
    public static void main(String[] args) {
        System.out.println("Lesson 5");
        System.out.println("Expressions\n");

        int friends = 10;

        friends = friends + 1;
        System.out.println(friends);

        friends = friends - 1;
        System.out.println(friends);

        friends = friends * 2;
        System.out.println(friends);

        friends = friends / 2;
        System.out.println(friends);

        friends = friends % 2;
        System.out.println(friends);

        friends--;
        System.out.println(friends);

        friends++;
        System.out.println(friends);
    }
}
